//混乱扑克
void CBaccaratLogic::RandCardList(BYTE cbCardBuffer[], BYTE cbBufferCount)
{
	//混乱准备
	BYTE cbCardData[CountArray(m_cbCardListData)];
	CopyMemory(cbCardData,m_cbCardListData,sizeof(m_cbCardListData));

	//混乱扑克
	BYTE cbRandCount=0,cbPosition=0;
	do
	{
		cbPosition=rand()%(CountArray(cbCardData)-cbRandCount);
		cbCardBuffer[cbRandCount++]=cbCardData[cbPosition];
		cbCardData[cbPosition]=cbCardData[CountArray(cbCardData)-cbRandCount];
	} while (cbRandCount<cbBufferCount);

	return;
}

//获取牌点
BYTE CBaccaratLogic::GetCardPip(BYTE cbCardData)
{
	//计算牌点
	BYTE cbCardValue=GetCardValue(cbCardData);
	BYTE cbPipCount=(cbCardValue>=10)?0:cbCardValue;

	return cbPipCount;
}

//获取牌点
BYTE CBaccaratLogic::GetCardListPip(const BYTE cbCardData[], BYTE cbCardCount)
{
	//变量定义
	BYTE cbPipCount=0;

	//获取牌点
	for (BYTE i=0;i<cbCardCount;i++)
	{
		cbPipCount=(GetCardPip(cbCardData[i])+cbPipCount)%10;
	}

	return cbPipCount;
}

//获取一张没出现过的牌
BYTE CBaccaratLogic::cbGetControlCard(BYTE cbCardPlayer[], BYTE cbCardCountPlayer, BYTE cbCardBanker[], BYTE cbCardCountBanker, BYTE cbSameCard)
{
	BYTE cbGetCardData = 0;

	//随机获取一张牌
	if (cbSameCard == 0)
	{
		bool bSuccess = true;
		BYTE cbGetCount = 0;
		do{
			BYTE cbRandCard = m_cbCardListData[rand() % CountArray(m_cbCardListData)];

			for (BYTE i = 0; i < cbCardCountPlayer;i ++)
			{
				if (cbRandCard == cbCardPlayer[i])
				{
					continue;
				}
			}

			for (BYTE i = 0; i < cbCardCountBanker; i++)
			{
				if (cbRandCard == cbCardBanker[i])
				{
					continue;
				}
			}

			cbGetCount++;

			if (cbGetCount > 200)
				break;

			cbGetCardData = cbRandCard;
			bSuccess = true;
		} while (!bSuccess);
	}
	//随机获取某一个点值的牌
	else
	{
		bool bSuccess = true;
		BYTE cbDispatchCount = 0;
		do{
			BYTE cbRandCard = cbSameCard & 0x0F;

			BYTE cbRandColor = rand() % 4;
			if (cbRandColor == 0)
				cbRandCard = cbRandCard;
			else if (cbRandColor == 1)
				cbRandCard = cbRandCard | 0x10;
			else if (cbRandColor == 2)
				cbRandCard = cbRandCard | 0x20;
			else if (cbRandColor == 3)
				cbRandCard = cbRandCard | 0x30;

			//怕出现一方出现3张 加上自己这方一张正好是四个相同数字的牌
			if (cbDispatchCount > 100)
			{
				cbGetCardData = cbRandCard;
				break;
			}

			for (BYTE i = 0; i < cbCardCountPlayer; i++)
			{
				if (cbRandCard == cbCardPlayer[i])
				{
					continue;
				}
			}

			for (BYTE i = 0; i < cbCardCountBanker; i++)
			{
				if (cbRandCard == cbCardBanker[i])
				{
					continue;
				}
			}
			cbGetCardData = cbRandCard;
			bSuccess = true;
		} while (!bSuccess);
	}

	return cbGetCardData;
}
